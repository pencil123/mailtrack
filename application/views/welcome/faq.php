    <li><a href="javascript:void(0)" class="list_title hua">如果使用Outlook发送追踪邮件的话，需要怎样变更Outlook的默认设置？</a>
        <div class="help_askcomment">
            <div class="help_ask_warp">
                <div class="arrow"></div><a class="a_close" href="javascript:void(0)"></a>
                <div class="askcomment_main">我们建议您直接使用网页版邮箱而不是邮箱客户端软件来嵌入外星人生成的追踪图片，因为邮箱客户端软件可能会增加您顺利使用外星人的难度。
                    <br>
                    <br>使用Outlook发送包含追踪图片的HTML邮件，默认是发送追踪图片的副本(作为附件发送)，而不是引用追踪图片的网络位置，这种情况下外星人无法追踪您的邮件。为此，您需要作出以下设置：
                    <br>
                    <br> Outlook Express：
                    <br>
                    <br>
                    <ol class="step-list">
                        <li>点击 <b>工具</b> > <b>选项</b>。</li>
                        <li>选择 <b>发送</b> 标签。</li>
                        <li>在 <b>邮件发送格式</b> 下，选中 <b>HTML</b>。</li>
                        <li>在 <b>邮件发送格式</b> 下，点击 <b>HTML设置</b>。</li>
                        <li>取消选中 <b>图片和邮件一同发送</b>。</li>
                        <li>点击 <b>确定</b> > <b>确定</b>。</li>
                    </ol>
                    <br>
                    <br> Outlook 2003：
                    <br>
                    <br>
                    <ol class="step-list">
                        <li>点击 <b>工具</b> > <b>选项</b>。</li>
                        <li>选择 <b>邮件格式</b> 标签。</li>
                        <li>在 <b>以该邮件格式撰写</b> 下选择 <b>HTML</b> 。</li>
                        <li>点击 <b>Internet格式</b>。</li>
                        <li>在 HTML选项 下取消选中 <b>在HTML邮件中包含Internet图片的情况下，发送图片的副本，而不是引用图片位置</b>。</li>
                        <li>在 Outlook RTF选项 下选择 <b>转换成HTML格式</b>。</li>
                        <li>点击 <b>确定</b> > <b>确定</b>。</li>
                    </ol>
                    <br>
                    <br> Outlook 2007：
                    <br>
                    <br>
                    <ol class="step-list">
                        <li>在 Windows Vista/Windows 7 下点击 <b>开始</b> 或在 Windows XP 下点击 <b>开始</b> > <b>运行</b>。</li>
                        <li>键入 <b>“</b>reg add HKEY_CURRENT_USER\Software\Microsoft\Office\12.0\Outlook\Options\Mail /v "Send Pictures With Document" /t reg_dword /d 0 /f<b>”</b>。</li>
                        <li>点击 <b>确定</b>。</li>
                    </ol>
                    <br>
                    <br> Outlook 2010：
                    <br>
                    <br>
                    <ol class="step-list">
                        <li>在 Windows Vista/Windows 7 下点击 <b>开始</b> 或在 Windows XP 下点击 <b>开始</b> > <b>运行</b>。</li>
                        <li>键入 <b>“</b>reg add HKEY_CURRENT_USER\Software\Microsoft\Office\14.0\Outlook\Options\Mail /v "Send Pictures With Document" /t reg_dword /d 0 /f<b>”</b>。</li>
                        <li>点击 <b>确定</b>。</li>
                    </ol>
